package com.jim.struct;

public class ForDemo01 {
    public static void main(String[] args) {
        int a = 1;
        while (a<=100){
            System.out.println(a);
            a+=1;
        }

        System.out.println("============");

        //100.for+enter 快捷方法
        for(int i = 1;i<=100;i++){
            System.out.println(i);
        }

    }
}
